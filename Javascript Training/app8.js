// Value Type Variabl
// When you work with arrays, they are stored as reference types in variables or in memory rather than the actual value being stored.  
// Primitive types (Variables, strings, booleans etc) are stored as the actual value in a variable. An array are stored as a reference to where the array is in memory.

// Const & Arrays
// As long as the reference doesn't change, the internals or insides of an array can change even if you're using const.
// You are able to alter the items in an array even with const. 
// Most array variables in the real world use const. 

